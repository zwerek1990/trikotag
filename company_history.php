            </li>
            <li>4 сентября было подано заявление на регистрацию компании Google в штате Калифорния.
            Вскоре после этого Ларри и Сергей открывают банковский счет на имя только что
            зарегистрированной компании и обналичивают чек Энди Бехтольшайма.
            </li>
            <li>Ларри и Сергей нанимают первого сотрудника. Им стал еще один выпускник факультета
            компьютерных технологий Стэнфордского университета&nbsp;– их сокурсник <a href=
            "http://web.archive.org/web/19991002122809/www-cs-students.stanford.edu/%7Ecsilvers/">
