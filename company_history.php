            1998
          </h2>
          <h3>
            Август
          </h3>
          <ul>
            <li>Энди Бехтольшайм, один из основателей компании Sun, выписывает <a href=
            "http://www.wired.com/science/discoveries/news/2007/09/dayintech_0907">чек на сумму 100
