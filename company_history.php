			<ul>
				<li>Это первенец трикотажной промышленности в Чувашской республике. В годы Великой Отечественной войны в г. Чебоксары Чувашской АССР была эвакуирована чулочная фабрика из г. Полтава Украинской ССР. 66 специалистов, прибывших вместе с эвакуированным оборудованием, с помощью местного населения в предельно сжатые сроки смонтировали оборудование, создали необходимую производственную базу и уже в начале 1942 года наладили производство очень нужной, прежде всего для фронта, продукции.</li>
				<li>На фабрике работало около 300 человек, выпуск 1942 года - 1 млн. пар чулочно-носочных изделий из хлопчатобумажной пряжи. Коллектив фабрики работал самоотверженно, строгом режиме работы по 10-12 часов ежедневно. Продолжался монтаж и пуск оборудования.</li>
