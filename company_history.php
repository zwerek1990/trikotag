            Google Inc.
            </li>
          </ul>
          <h3>
            Сентябрь
          </h3>
          <ul>
            <li>Компания Google обустраивается в гараже Сьюзен Войцицки по адресу <a href=
            "http://maps.google.com/maps?q=232+santa+margarita,+menlo+park+ca&amp;ie=UTF8&amp;oe=utf-8&amp;client=firefox-a&amp;ll=37.457861,-122.163312&amp;spn=0.008431,0.019999&amp;z=16&amp;iwloc=addr">
