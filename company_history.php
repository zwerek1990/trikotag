	  <h2 class="section" id="1995-1997">
            1995-1997
          </h2>
          <h3>
            1995
          </h3>
          <ul>
            <li>Ларри Пейдж и Сергей Брин впервые встретились в Стэнфордском университете Ларри,
            22-летний выпускник Мичиганского университета, собирался продолжить в Стэнфорде свое
            образование, а Сергей, которому к тому моменту исполнился 21 год, показывал ему
            университет. Говорят, в тот раз они разошлись во мнениях по всем вопросам.
            </li>
          </ul>
          <h3>
            1996
          </h3>
          <ul>
            <li>Ларри и Сергей, окончившие Стэнфорд со специализацией в области компьютерных
            технологий, начинают совместную разработку поискового механизма под названием
              <a href="http://web.archive.org/web/19971210065425/backrub.stanford.edu/backrub.html">
              BackRub</a>.
            </li>
            <li>Более года система BackRub работала на серверах Стэнфорда, и в конечном итоге ее
            трафик превысил пропускную способность университетского интернет-канала.
            </li>
          </ul>
          <h3>
            1997
          </h3>
          <ul>
            <li>Ларри и Сергей пришли к мнению, что поисковой системе BackRub требуется новое
            название. После долгих размышлений они остановились на слове <a href=
            "http://web.archive.org/web/19990125084553/alpha.google.com/">Google</a>. В нем
            обыгрывается математический термин "гугол" (googol), обозначающий число десять в сотой
            степени, то есть единицу со ста нулями. В названии системы воплотилось стремление ее
            создателей упорядочить почти безграничные объемы информации в Интернете.
            </li>
          </ul><a class="company-top-link" href="#top">К началу</a>
